<h4>Getting Here</h4>
<h5>Buffalo-Niagara International Airport (BUF)</h5>

<p>The Buffalo airport is a very affordable airport to fly into. The airport is about a 20 minute drive from downtown Buffalo. If you do not want to rent a car, it is not necessary. Taxis are readily available to be hailed or called in downtown Buffalo. A taxi ride from the airport will run you about $50 each way, so we would recommend checking with your hotel first, as many downtown Buffalo hotels have an airport shuttle service. You can also book a shuttle through <a href="http://www.buffaloairporttaxi.com/our-services">Buffalo Airport Taxi</a> that will take you to downtown Buffalo for $21 each way.</p>

<h5>Crossing the border</h5>

<p>For our Canadian guests, please don't forget that you will need a passport to cross the U.S. border. If you will be crossing the border the day of the wedding, we recommend leaving yourself plenty of time. Although our wedding is only about ten minutes from the border on the U.S. side, you should allow some extra time in case of any delays at the border (whether it's long line-ups, or being randomly called into secondary questioning.)</p>

<p>For our American guests, don't forget to bring your passports if you want to do any Canadian sightseeing during your visit! Downtown Buffalo is only ten minutes from the Canadian border, and Niagara Falls is only 30 minutes away.</p>

